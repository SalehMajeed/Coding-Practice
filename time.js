function time(arr) {
	let res = '';
	let seconds = 0;
	let minutes = 0;
	let hours = 0;
	for (let i = 0; i < arr.length; i++) {
		const split_res = arr[i].split(':');
		seconds += +split_res.pop();
		minutes += +split_res.pop();
		hours += +split_res.pop();
	}
	hours = hours * 60 * 60;
	minutes = minutes * 60;
	seconds += hours + minutes;
	return seconds / 60 / 60;
}

